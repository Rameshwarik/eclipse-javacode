package com.day1;

public class variabledeclaration {

	public static void main(String[] args) {
		/*VARIABLE DECLARATION:
		 * keywords are not allowable to use as variable;(int for a)
		 * Spaces are not allowed: (int num 2);
		 * Special symbols are not allowed: (int @num =2)
		 */
		int $num = 10;
		System.out.println($num);
		
		String studentName = "Rameshwari";
		System.out.println(studentName);
		
				

	}

}
