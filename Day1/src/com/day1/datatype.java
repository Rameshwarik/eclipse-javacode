package com.day1;

public class datatype {

	public static void main(String[] args) {
		float value1 = 9/2f ; //integer division
		float value2 = 101/61f; //include the initial f
		double value3 = 10d/4d; //include the initial d
		float value4 = 5/2.5f; //floor division
		
		System.out.println(value1);
		System.out.println(value2);
		System.out.println(value3);
		System.out.println(value4);
		
		int marker = 512;
		// assigning expression to variable
		
		double percentage = marker * 0.46f;
		System.out.println(percentage);
	}

}
